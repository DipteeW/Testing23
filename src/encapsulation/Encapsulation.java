package encapsulation;

public class Encapsulation {
	private int a=10;
	private int b=20;
	public void sample()
	{
		System.out.println(a);
		System.out.println(b);
	}
	public void addition(int i,int j)
	{
		System.out.println(i+j);
	}
	public void multi(int i,int j)
	{
		System.out.println(i*j);
	}
}
